class Q2Object {
    int[] risk;
    int[] bonus;
    int[] trader;

    public Q2Object() {
    }

    public int[] getRisk() {
        return risk;
    }

    public void setRisk(int[] risk) {
        this.risk = risk;
    }

    public int[] getBonus() {
        return bonus;
    }

    public void setBonus(int[] bonus) {
        this.bonus = bonus;
    }

    public int[] getTrader() {
        return trader;
    }

    public void setTrader(int[] trader) {
        this.trader = trader;
    }
}
